    public void paceinteEditar() {
        try{
         
            Registry registro=LocateRegistry.getRegistry("127.0.0.1",7777);
            DaoLogin interfaz = (DaoLogin) registro.lookup("RemotoRMI");
            pacienAEditar=interfaz.pacienteLogeado();
            jTextFielNombre.setText(pacienAEditar.getNombre());
            jTextFielCedula.setText(pacienAEditar.getCedula());
            jTextFielEmail.setText(pacienAEditar.getEmail());
            jComboBoxSangre.setSelectedItem(pacienAEditar.getTipoDeSangre());
            
        }catch(Exception e){
            System.out.println("estamos en excepcion dentro de paceinte"+e);
        }
        
   }
