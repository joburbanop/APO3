   public void paceinteLogeado() {
        try{
            Registry registro=LocateRegistry.getRegistry("127.0.0.1",7777);
            DaoLogin interfaz = (DaoLogin) registro.lookup("RemotoRMI");
            pacienteLog=interfaz.pacienteLogeado();
            jLabelEmail.setText(pacienteLog.getEmail());
            jLabelCedula.setText(pacienteLog.getCedula());
            jLabelTipoDeSangre.setText(pacienteLog.getTipoDeSangre());
            jLabelNombre.setText(jLabelNombre.getText()+ pacienteLog.getNombre());
        }catch(Exception e){
            System.out.println("estamos en excepcion dentro de paceinte"+e);
        }
        
   }
