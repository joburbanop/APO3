    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            Registry registro=LocateRegistry.getRegistry("127.0.0.1",7777);
            DaoLogin interfaz = (DaoLogin) registro.lookup("RemotoRMI");

            
            if(interfaz.verificarUsuario(jTextFieldNombre.getText(), jTextFieldCedula.getText())){
                super.setVisible(false);
                PacienteInterfaz rol= PacienteInterfaz.getPacienteInterfaz();
                rol.setVisible(true); 
                
            }else{
                JOptionPane.showMessageDialog(null, "Paciente no registrado");
 
            }

        }catch(Exception e){
            System.out.println("estamos en excepcion"+e);
        }
    
        
    }//GEN-LAST:event_jButton3ActionPerformed

    
