    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO code application logic here
        try{
            Registry registro=LocateRegistry.getRegistry("127.0.0.1",7777);
            DaoLogin interfaz = (DaoLogin) registro.lookup("RemotoRMI");

            //System.out.println("aca si");
           // Crear un nuevo objeto Paciente y establecer sus valores
            
            System.out.println(EntradaNombre.getText());
            System.out.println(EntradaEmail.getText());
            System.out.println(EntradaCedula.getText());
            if(!interfaz.verificarUsuario(EntradaNombre.getText(), EntradaCedula.getText())){
                Paciente nuevoPaciente = new Paciente();
                nuevoPaciente.setNombre(EntradaNombre.getText());
                nuevoPaciente.setTipoDeSangre(EntradaSangre.getSelectedItem().toString());
                nuevoPaciente.setEmail(EntradaEmail.getText());
                nuevoPaciente.setCedula(EntradaCedula.getText());
                interfaz.agregar(nuevoPaciente);
                interfaz.agregarEnArchivo(nuevoPaciente);
                JOptionPane.showMessageDialog(null, "El paciente fue registrado correctamente.");
            }else{
                JOptionPane.showMessageDialog(null, "Paciente ya se ha registrado o faltan datos");
 
            }

        }catch(Exception e){
            System.out.println("estamos en excepcion"+e);
        }
    
        

        
        
       
    }//GEN-LAST:event_jButton1ActionPerformed

